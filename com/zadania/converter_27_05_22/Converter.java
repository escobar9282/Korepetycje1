package com.zadania.converter_27_05_22;

public class Converter {
    double metersToCentimeters(double meters) {
        return meters * 100;
    }

    double metersToMillimeters(double meters) {
        return meters * 1000;
    }

    double cmToM(double cm) {
        return cm / 100;
    }

    double mmToM(double mm) {
        return mm / 1000;
    }
}
