package com.zadania.Cwiczenia;

public class Cw_003 {
    public static void main(String[] args) {
        int degree = 7;
        switch (degree) {
            case 1 :
                System.out.println("Weak.");
                break;
            case 2 :
                System.out.println("It could be better.");
                break;
            case 3 :
                System.out.println("Not so bad.");
                break;
            case 4 :
                System.out.println("Good.");
                break;
            case 5 :
                System.out.println("The best");
                break;
            case 6 :
                System.out.println("Brilliant");
            default:
                System.out.println("You've entered a wrong value. Correct it.");


        }
    }
}
