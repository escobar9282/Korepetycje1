package com.zadania.zadanie3_8;

public class Main {
    public static void main(String[] args) {
        int sum = 0;
        int i = 1;
        while (i <= 100) {
            sum = sum + i;
            i++;
        }

        System.out.println("Wynik to " + sum);
    }
}
